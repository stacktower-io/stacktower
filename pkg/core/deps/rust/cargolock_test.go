func TestCargoLock_Supports(t *testing.T) {
	c := &CargoLock{}

	tests := []struct {
		name     string
		filename string
		want     bool
	}{
		{"exact match", "Cargo.lock", true},
		{"lowercase", "cargo.lock", true},
		{"Cargo.toml", "Cargo.toml", false},
		{"other file", "package.json", false},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			if got := c.Supports(tt.filename); got != tt.want {
				t.Errorf("Supports(%q) = %v, want %v", tt.filename, got, tt.want)
			}
		})
	}
}

