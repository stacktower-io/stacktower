func TestParseCargoLockDep(t *testing.T) {
	tests := []struct {
		spec        string
		wantName    string
		wantVersion string
	}{
		{"libc 0.2.149", "libc", "0.2.149"},
		{"libc 0.2.149 (registry+https://github.com/rust-lang/crates.io-index)", "libc", "0.2.149"},
		{"serde", "serde", ""},
		{"", "", ""},
	}

	for _, tt := range tests {
		t.Run(tt.spec, func(t *testing.T) {
			name, version := parseCargoLockDep(tt.spec)
			if name != tt.wantName {
				t.Errorf("name = %q, want %q", name, tt.wantName)
			}
			if version != tt.wantVersion {
				t.Errorf("version = %q, want %q", version, tt.wantVersion)
			}
		})
	}
}

func TestCargoLock_RuntimeVersion(t *testing.T) {
	lockContent := `version = 3

[[package]]
name = "my-crate"
version = "0.1.0"
dependencies = ["serde"]

[[package]]
name = "serde"
version = "1.0.193"
source = "registry+https://github.com/rust-lang/crates.io-index"
`
	cargoTomlContent := `[package]
name = "my-crate"
version = "0.1.0"
rust-version = "1.70.0"

[dependencies]
serde = "1.0"
`
	tmpDir := t.TempDir()
	lockPath := filepath.Join(tmpDir, "Cargo.lock")
	if err := os.WriteFile(lockPath, []byte(lockContent), 0644); err != nil {
		t.Fatal(err)
	}
	tomlPath := filepath.Join(tmpDir, "Cargo.toml")
	if err := os.WriteFile(tomlPath, []byte(cargoTomlContent), 0644); err != nil {
		t.Fatal(err)
	}

	c := &CargoLock{}
	result, err := c.Parse(lockPath, deps.Options{})
	if err != nil {
		t.Fatalf("Parse() error = %v", err)
	}

	if result.RootPackage != "my-crate" {
		t.Errorf("RootPackage = %q, want %q", result.RootPackage, "my-crate")
	}
	if result.RuntimeVersion != "1.70.0" {
		t.Errorf("RuntimeVersion = %q, want %q", result.RuntimeVersion, "1.70.0")
	}
	if result.RuntimeConstraint != ">=1.70.0" {
		t.Errorf("RuntimeConstraint = %q, want %q", result.RuntimeConstraint, ">=1.70.0")
	}
}

